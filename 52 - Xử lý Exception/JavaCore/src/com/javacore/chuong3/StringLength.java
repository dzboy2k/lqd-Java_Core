package com.javacore.chuong3;

public class StringLength {

  public static void main(String[] args) {

    //Tạo một đối tượng String
    String str = "Hello";

    // Tính chiều dài của chuỗi
    int length = str.length();
    System.out.println("Length = " + length);
    // Kết quả : Length = 5
  }
}
